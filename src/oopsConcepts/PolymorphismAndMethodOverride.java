package oopsConcepts;

public class PolymorphismAndMethodOverride {
	
	/*
	 * Method override - Method overriding can only be achievable in parent child relation ship. 
	 * When we have same method name with same number of arguments & type of arguments in both parent & child is known as overriding.
	 * 
	 * 
	 * Polymorphism - Using this concept we can perform single action in different way.
	 * Types of polymorphism:
	 * 
	 * 1.Compile time (or) Static polymorphism:
	 * 			It can be achieved in the form of method overloading.
	 * 
	 * 			Static binding - it can be identified/resolved during compile time by compiler. (note: When we create an reference & object for same class and invoking the method)
	 * 
	 * 2.Run time (or) Dynamic polymorphism
	 * 			It can be achieved in the form of method overriding.
	 * 			When we create a reference for parent and object for child class and invoking the override method is known as run time polymorphism.
	 * 			
	 * 			Dynamic binding - it can't be identified by the compiler & it will be identified during runtime.
	 * 				Note: When we create an reference for parent & object for child and invoking the override method.
	 * 
	 *
	 *
	 */

}
