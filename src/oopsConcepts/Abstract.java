package oopsConcepts;

public class Abstract {
	
	/* Abstract is a keyword using which we can hide the implementation & show the functionality to the user.
	 * Abstract keyword can be used with method & class
	 * Abstract class can have abstract method & non-abstract (Concrete Method) method
	 * When the class has one abstract method then the class should be an abstract
	 * When we declare a method as an abstract then we shouldn't define that method. The defination should be afford in the child class.
	 * We can't create an object for an abstract class, but we can create an reference for it.
	 * Abstract class can have a constructor
	 * Abstract keyword can't be used with stactic keyowrd & final keyword.
	 * 
	 * 
	 * 
	 * What is an abstract method?
	 * 		A method without an implementation
	 * 		A method without an defination
	 * 		A method with an abstract keyword
	 * 
	 * What is an abstract class?
	 * 		A class with one or more abstract method.
	 * 
	 */

}
